package day01_self_practice;
/*
Create a Java class named PrintExecution that prints the following outputs in the console:

	Cydeo

	Yuliia
	Alena
	Lucy
	Alexander

	Igor
	Sumeye
	Madina
 */

public class PrintExecution {
    public static void main(String[] args) {
        System.out.println();
        System.out.println("\tCydeo");
        System.out.println();
        System.out.println("\tYuliia");
        System.out.println("\tAlena");
        System.out.println("\tLucy");
        System.out.println("\tAlexander");
        System.out.println();
        System.out.println("\tIgor");
        System.out.println("\tSumeye");
        System.out.println("\tMadina");

    }
}
