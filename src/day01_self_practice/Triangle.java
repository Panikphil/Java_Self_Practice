package day01_self_practice;
/*
Create a Java class named Triangle that prints the following outputs in the console:

	                    ^
	                   / \
	                  /   \
	                 /     \
	                /       \
	                ---------

 */

public class Triangle {
    public static void main(String[] args){

        System.out.println();
        System.out.println("\t                    ^");
        System.out.println("\t                   / \\");
        System.out.println("\t                  /   \\");
        System.out.println("\t                 /     \\");
        System.out.println("\t                /       \\");
        System.out.println("\t                ---------");
    }
}
